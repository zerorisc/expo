.globl main
main:
  /* Init all-zero register. */
  bn.xor  w31, w31, w31
  
  /* MOD <= dmem[modulus] = DILITHIUM_Q */
  li      x5, 2
  la      x6, modulus
  bn.lid  x5, 0(x6)

  /* MOD 2nd word <= DILITHIUM_R */
  li      x5, 3
  la      x6, montg_R
  bn.lid  x5, 0(x6)
  bn.rshi w2, w3, w2 >> 224
  /* Write back MOD */
  bn.wsrw 0x0, w2

  /* Load stack address */
  la    x2, stack_end
  /* Load parameters */
  la    x10, signature
  li    x11, CRYPTO_BYTES  /* siglen */
  la    x12, message
  la    x13, messagelen
  lw    x13, 0(x13) /* msglen */
  /* addi  x13, x0, 33   */
  la    x14, pk
  /* Prepare context */
  la x15, ctx
  li x16, CTXLEN

  jal x1, verify_dilithium

  ecall

